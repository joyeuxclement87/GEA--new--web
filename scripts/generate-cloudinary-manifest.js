#!/usr/bin/env node
/**
 * Scans the codebase for every cld('public/id', { w: N }) call and generates
 * a CLOUDINARY_ASSETS.md manifest listing each public ID, its expected width,
 * and which file(s) reference it — so real images can be uploaded to
 * Cloudinary under matching public IDs.
 *
 * Run with: node scripts/generate-cloudinary-manifest.js
 */

const fs = require("fs");
const path = require("path");

const SRC_DIR = path.join(__dirname, "..", "src");
const OUTPUT_FILE = path.join(__dirname, "..", "CLOUDINARY_ASSETS.md");
const CLOUD_NAME_PLACEHOLDER = "<your-cloud-name>";

/** Recursively collect all .ts/.tsx files under a directory. */
function collectFiles(dir) {
  let results = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      results = results.concat(collectFiles(full));
    } else if (/\.(ts|tsx)$/.test(entry.name)) {
      results.push(full);
    }
  }
  return results;
}

// Matches: cld('some/public-id', { w: 1234 })  — width is optional
const CLD_CALL_REGEX = /cld\(\s*['"]([^'"]+)['"]\s*(?:,\s*\{\s*w:\s*(\d+)[^}]*\})?\s*\)/g;

function main() {
  const files = collectFiles(SRC_DIR);
  /** @type {Map<string, { widths: Set<number>, files: Set<string> }>} */
  const assets = new Map();

  for (const file of files) {
    if (file.endsWith(path.join("lib", "cloudinary.ts"))) continue; // skip doc-comment examples
    const content = fs.readFileSync(file, "utf8");
    let match;
    while ((match = CLD_CALL_REGEX.exec(content)) !== null) {
      const [, publicId, width] = match;
      const relFile = path.relative(path.join(__dirname, ".."), file);
      if (!assets.has(publicId)) {
        assets.set(publicId, { widths: new Set(), files: new Set() });
      }
      const entry = assets.get(publicId);
      if (width) entry.widths.add(Number(width));
      entry.files.add(relFile);
    }
  }

  const sortedIds = Array.from(assets.keys()).sort();

  // Group by top-level section — public IDs are flat (hyphen-joined), so the
  // section is the first hyphen segment (e.g. "heroes-services-hvac" -> "heroes").
  /** @type {Map<string, string[]>} */
  const sections = new Map();
  for (const id of sortedIds) {
    const section = id.includes("-") ? id.split("-")[0] : "misc";
    if (!sections.has(section)) sections.set(section, []);
    sections.get(section).push(id);
  }

  const lines = [];
  lines.push("# Cloudinary Asset Manifest");
  lines.push("");
  lines.push(
    "This file is auto-generated by `scripts/generate-cloudinary-manifest.js`. " +
      "It lists every Cloudinary public ID referenced in the codebase via the `cld()` helper " +
      "(`src/lib/cloudinary.ts`), so real images can be uploaded to Cloudinary under matching paths."
  );
  lines.push("");
  lines.push(
    `**Total unique image slots: ${sortedIds.length}**`
  );
  lines.push("");
  lines.push("## Setup");
  lines.push("");
  lines.push(
    "1. Create/use a Cloudinary account and note your **cloud name**."
  );
  lines.push(
    "2. Set `NEXT_PUBLIC_CLOUDINARY_CLOUD_NAME=<your-cloud-name>` in your `.env.local`."
  );
  lines.push(
    "3. Upload each image below to Cloudinary using the **exact Public ID** shown " +
      "(flat public IDs, no folders — e.g. `heroes-services-mep-engineering`)."
  );
  lines.push(
    "4. Once uploaded, the corresponding page will automatically pick up the image — no code changes needed."
  );
  lines.push("");
  lines.push(
    `A preview/delivery URL follows the pattern: \`https://res.cloudinary.com/${CLOUD_NAME_PLACEHOLDER}/image/upload/f_auto,q_auto,w_<width>,c_fill/<public-id>\``
  );
  lines.push("");
  lines.push("---");
  lines.push("");

  const sortedSections = Array.from(sections.keys()).sort();

  for (const section of sortedSections) {
    lines.push(`## ${sectionTitle(section)}`);
    lines.push("");
    lines.push("| Public ID | Width(s) used | Referenced in |");
    lines.push("|---|---|---|");
    for (const id of sections.get(section)) {
      const entry = assets.get(id);
      const widths = Array.from(entry.widths).sort((a, b) => a - b).join(", ") || "—";
      const files = Array.from(entry.files).sort().join("<br>");
      lines.push(`| \`${id}\` | ${widths} | ${files} |`);
    }
    lines.push("");
  }

  fs.writeFileSync(OUTPUT_FILE, lines.join("\n"));
  console.log(`Wrote ${sortedIds.length} asset entries to ${path.relative(process.cwd(), OUTPUT_FILE)}`);
}

function sectionTitle(section) {
  const map = {
    heroes: "Hero Images",
    services: "Service Pages",
    solutions: "Solutions Pages",
    projects: "Projects",
    team: "Team Photos",
    testimonials: "Testimonials",
    insights: "Insights / Blog",
    nav: "Navigation (Mega Menus)",
    about: "About Page",
    contact: "Contact Page",
    misc: "Miscellaneous",
  };
  return map[section] || section.charAt(0).toUpperCase() + section.slice(1);
}

main();
